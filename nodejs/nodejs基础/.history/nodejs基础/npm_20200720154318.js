/* 
    全局安装 -g
    全局安装的包位于Node.js环境的node_moudles目录下,全局安装的包一般用于命令行工具
    本地安装 
    本地安装的包在当前目录下的 node_moudles里面
    npm常用的命令
    1,安装包(如果没有指定版本号,那么安装到最新版本)
    npm install -g 包名称(全局安装)
    npm install 包名称 (本地安装)

    2,安装包的时候可以指定版本
    npm  install -g 包名称@版本号

    3,卸载包
    npm uninstall -g 包名

    4,更新包(更新到最新版本)
    npm update -g 包名

*/